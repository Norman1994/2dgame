
public class Otpusk {
	
	public int god;
	
	public String fio;
	
	public String uch;
	
	public String svoi;
	
	public String vneoch;
	
	public Otpusk( int god, String fio, String uch, String svoi, String vneoch)
	{
		this.god = god;
		this.fio = fio;
		this.uch = uch;
		this.svoi = svoi;
		this.vneoch = vneoch;
	}

	public int getGod() {
		return god;
	}

	public void setGod(int god) {
		this.god = god;
	}

	public String getFio() {
		return fio;
	}

	public void setFio(String fio) {
		this.fio = fio;
	}

	public String getUch() {
		return uch;
	}

	public void setUch(String uch) {
		this.uch = uch;
	}

	public String getSvoi() {
		return svoi;
	}

	public void setSvoi(String svoi) {
		this.svoi = svoi;
	}

	public String getVneoch() {
		return vneoch;
	}

	public void setVneoch(String vneoch) {
		this.vneoch = vneoch;
	}
	

}
